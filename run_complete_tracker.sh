import smtplib
from email.mime.text import MIMEText
from email.mime.multipart import MIMEMultipart
from datetime import datetime
import os
import sys

def send_report_email():
    try:
        from scripts.email_config import RECIPIENTS, EMAIL_SERVER_CONFIG
        
