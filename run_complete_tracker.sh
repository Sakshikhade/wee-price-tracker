        with open(report_file, 'r') as f:
            report_content = f.read()
        
        print(f"ğŸ“„ Report content length: {len(report_content)} characters")
        
        # Send to all enabled recipients
        enabled_recipients = [r for r in RECIPIENTS if r['enabled']]
        
        if not enabled_recipients:
            print("âŒ No enabled recipients found")
            return False
        
        print(f"ğŸ“§ Sending to {len(enabled_recipients)} recipients...")
        
